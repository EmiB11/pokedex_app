package com.pokemon.backend.models;

public class DataObject {
    private String name;
    private String url;

    public DataObject(){}

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
    
}
